    /**
     * Method untuk mengirim pesan ke SiTandi dan menampilkan respons
     */
    private void kirimPesan() {
        // Ambil pesan dari input field
        String pesan = inputField.getText();

        // Validasi input - jika input kosong
        if (pesan == null || pesan.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this,
                "Silakan ketik pesan terlebih dahulu",
                "Peringatan",
                JOptionPane.WARNING_MESSAGE);
            inputField.requestFocus();
            return;
        }

        final String pesanFinal = pesan.trim();

        // Tampilkan pesan user di area percakapan
        messageField.append(">>> Anda: " + pesanFinal + "\n");

        // Kosongkan input field
        inputField.setText("");

        // Disable tombol dan tampilkan loading cursor
        jButton1.setEnabled(false);
        inputField.setEnabled(false);
        this.setCursor(Cursor.getPredefinedCursor(Cursor.WAIT_CURSOR));

        // Kirim ke SiTandi menggunakan thread terpisah agar UI tidak freeze
        new Thread(() -> {
            try {
                String response = geminiService.generateResponse(pesanFinal);

                // Update UI di Event Dispatch Thread
                java.awt.EventQueue.invokeLater(() -> {
                    messageField.append(">>> SiTandi: " + response + "\n\n");
                    messageField.setCaretPosition(messageField.getDocument().getLength());
                    jButton1.setEnabled(true);
                    inputField.setEnabled(true);
                    inputField.requestFocus();
                    ChatGemini.this.setCursor(Cursor.getDefaultCursor());
                });

            } catch (Exception e) {
                // Update UI dengan error message
                java.awt.EventQueue.invokeLater(() -> {
                    messageField.append("!!! Error: " + e.getMessage() + "\n\n");
                    jButton1.setEnabled(true);
                    inputField.setEnabled(true);
                    inputField.requestFocus();
                    ChatGemini.this.setCursor(Cursor.getDefaultCursor());

                    JOptionPane.showMessageDialog(ChatGemini.this,
                        "Terjadi kesalahan: " + e.getMessage(),
                        "Error",
                        JOptionPane.ERROR_MESSAGE);
                });
                System.out.println("Error saat mengirim pesan: " + e.getMessage());
            }
        }).start();
    }

